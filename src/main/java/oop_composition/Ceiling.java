package oop_composition;

public class Ceiling {
    private int hight;
    private int paintedColor;

    public Ceiling(int hight, int paintedColor) {
        this.hight = hight;
        this.paintedColor = paintedColor;
    }

    public int getHight() {
        return hight;
    }

    public int getPaintedColor() {
        return paintedColor;
    }
}
